#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	len;

	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	else
	{
		len = ft_strlen(s);
		while (*s)
			write(1, s++, 1);
	}
	return (len);
}
