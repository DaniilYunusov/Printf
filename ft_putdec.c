#include "ft_printf.h"

int	ft_putdec(int n)
{
	int	res;
	int	copy_n;

	res = 1;
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		n *= -1;
		ft_putchar('-');
		res++;
	}
	if (n / 10 > 0)
		res += ft_putdec(n / 10);
	copy_n = n % 10 + '0';
	write(1, &copy_n, 1);
	return (res);
}
