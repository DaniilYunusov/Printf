#include "ft_printf.h"

int	ft_putudec(unsigned int n)
{
	int	res;
	int	copy_n;

	res = 1;
	if (n / 10 > 0)
		res += ft_putudec(n / 10);
	copy_n = n % 10 + '0';
	write(1, &copy_n, 1);
	return (res);
}
